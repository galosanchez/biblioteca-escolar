    /**
     * MÃ©todo que permite validar los datos que el usuario ingresa antes de
     * enviar hacia la base de datos. Este retornara un boolean para verificar
     * que todos los campos esten correctamente ingresados.
     *
     * @return
     */
    private boolean validarCamposFormato() {
        //variable de verificaciÃ³n de los campos
        boolean verif = true;
        /* ValidaciÃ³n del nombre: no debe quedar vacio */
        if (jTextFieldName.getText().length() < 1) {
            verif = false;
            jLabelErrorName.setText("Olvidaste ingresar el nombre.");
        }
        /* ValidaciÃ³n del apellido: no debe quedar vacio */
        if (jTextFieldLastName.getText().length() < 1) {
            verif = false;
            jLabelErrorLastName.setText("Olvidaste ingrese el apellido.");
        }
        
        return verif;
    }

    /**
     * Metodo que permite limpiar mensaje de error de los jLabels
     */
    private void cleanjLabelsError() {
        jLabelErrorName.setText("");
        jLabelErrorLastName.setText("");
    }

    /**
     * Metodo que permite limpiar todos los campos de TextField
     */
    private void cleanjTextFields() {
        jTextFieldName.setText("");
        jTextFieldLastName.setText("");
    }

    /**
     * Metodo que valida si un correo electronico tiene el formaro correcto
     *
     * @param email
     * @return
     */
    public static boolean ValidarMail(String email) {
        Pattern pattern = Pattern.compile("^[_A-Za-z0-9-\\+]+(\\.[_A-Za-z0-9-]+)*@[A-Za-z0-9-]+(\\.[A-Za-z0-9]+)*(\\.[A-Za-z]{2,})$");
        Matcher mather = pattern.matcher(email);
        return mather.find();
    }

